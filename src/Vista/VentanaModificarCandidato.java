    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        int index = cbxSeleccionarCandidato.getSelectedIndex();
        if (index >= 0) {
            String dni = txtDNI.getText();
            if (!dni.matches("\\d{8}")) {
                JOptionPane.showMessageDialog(this, "El DNI debe ser un número de 8 dígitos.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            Candidato candidato = GestionCandidato.getLista().get(index);
            candidato.setNombre(txtNombre.getText());
            candidato.setApellidoPaterno(txtApellidoPaterno.getText());
            candidato.setApellidoMaterno(txtApellidoMaterno.getText());
            candidato.setDNI(dni);
            candidato.setPartidoPolitico(txtPartido.getText());
            JOptionPane.showMessageDialog(this, "Candidato actualizado correctamente.");
            txtNombre.setEditable(false);
            txtApellidoPaterno.setEditable(false);
            txtApellidoMaterno.setEditable(false);
            txtDNI.setEditable(false);
            txtPartido.setEditable(false);
            cargarCandidatos();
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int index = cbxSeleccionarCandidato.getSelectedIndex();
        if (index >= 0) {
            int confirm = JOptionPane.showConfirmDialog(this, "¿Está seguro de eliminar este candidato?");
            if (confirm == JOptionPane.YES_OPTION) {
                GestionCandidato.getLista().remove(index);
                JOptionPane.showMessageDialog(this, "Candidato eliminado correctamente.");
                cargarCandidatos();
                limpiar();
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

