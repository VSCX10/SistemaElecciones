    private void cargarCandidatos() {
        cbxSeleccionarCandidato.removeAllItems();
        LinkedList<Candidato> lista = GestionCandidato.getLista();
        if (lista.isEmpty()) {
            cbxSeleccionarCandidato.addItem("No hay candidatos disponibles");
        } else {
            for (Candidato candidato : lista) {
                cbxSeleccionarCandidato.addItem(candidato.getNombre() + " " + candidato.getApellidoPaterno() + " " + candidato.getApellidoMaterno());
            }
        }
    }

    private void cargarDatosCandidato(int index) {
        LinkedList<Candidato> lista = GestionCandidato.getLista();
        if (index >= 0 && index < lista.size()) {
            Candidato candidato = lista.get(index);
            txtNombre.setText(candidato.getNombre());
            txtApellidoPaterno.setText(candidato.getApellidoPaterno());
            txtApellidoMaterno.setText(candidato.getApellidoMaterno());
            txtDNI.setText(candidato.getDNI());
            txtPartido.setText(candidato.getPartidoPolitico());
        }
    }
    
    private void limpiar() {
        txtNombre.setText("");
        txtApellidoPaterno.setText("");
        txtApellidoMaterno.setText("");
        txtDNI.setText("");
        txtPartido.setText("");
    }

