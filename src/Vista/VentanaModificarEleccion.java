    private void btnEliminarEleccionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarEleccionActionPerformed
        int index = cbxSeleccionarEleccion.getSelectedIndex();
        if (index < 0 || GestionEleccion.getLista().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Seleccione una elección válida para eliminar.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        int confirm = JOptionPane.showConfirmDialog(this, "¿Está seguro de eliminar esta elección?", "Confirmación", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            Eleccion eleccion = GestionEleccion.getLista().get(index);
            GestionEleccion.getLista().remove(eleccion);
            JOptionPane.showMessageDialog(this, "Elección eliminada correctamente.", "Éxito", JOptionPane.INFORMATION_MESSAGE);
            cargarElecciones();
            limpiar();
            if (GestionEleccion.getLista().isEmpty()) {
                cbxSeleccionarEleccion.setSelectedIndex(-1);
            } else {
                cbxSeleccionarEleccion.setSelectedIndex(0);
                cbxSeleccionarEleccionActionPerformed(null);
            }
        }
    }//GEN-LAST:event_btnEliminarEleccionActionPerformed

    private void cbxSeleccionarEleccionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbxSeleccionarEleccionActionPerformed
        int index = cbxSeleccionarEleccion.getSelectedIndex();
        if (index >= 0 && !GestionEleccion.getLista().isEmpty()) {
            Eleccion eleccion = GestionEleccion.getLista().get(index);
            SimpleDateFormat formatoFecha = new SimpleDateFormat("dd/MM/yyyy");
            txtFecha.setText(formatoFecha.format(eleccion.getFecha()));
            cbxTipoEleccion.setSelectedItem(eleccion.getTipo());
        }
    }//GEN-LAST:event_cbxSeleccionarEleccionActionPerformed

    public void cargarElecciones() {
        cbxSeleccionarEleccion.removeAllItems();
        LinkedList<Eleccion> lista = GestionEleccion.getLista();
        SimpleDateFormat formatoFecha = new SimpleDateFormat("dd/MM/yyyy");
        if (lista.isEmpty()) {
            cbxSeleccionarEleccion.addItem("No hay elecciones disponibles");
            cbxSeleccionarEleccion.setEnabled(false);
            txtFecha.setEnabled(false);
            cbxTipoEleccion.setEnabled(false);
            return;
        }
        cbxSeleccionarEleccion.setEnabled(true);
        txtFecha.setEnabled(true);
        cbxTipoEleccion.setEnabled(true);
        for (Eleccion eleccion : lista) {
            String eleccionTexto = formatoFecha.format(eleccion.getFecha()) + " - " + eleccion.getTipo();
            cbxSeleccionarEleccion.addItem(eleccionTexto);
        }
    }

    public void limpiar() {
        txtFecha.setText("");
        cbxTipoEleccion.setSelectedIndex(0);
    }

