    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        int index = cbxSeleccionarEleccion.getSelectedIndex();
        if (index < 0 || GestionEleccion.getLista().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Seleccione una elección válida.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        String fechaTexto = txtFecha.getText();
        String nuevoTipo = (String) cbxTipoEleccion.getSelectedItem();
        SimpleDateFormat formatoFecha = new SimpleDateFormat("dd/MM/yyyy");
        formatoFecha.setLenient(false);
        try {
            Date nuevaFecha = formatoFecha.parse(fechaTexto);
            Date fechaActual = new Date();
            Eleccion eleccion = GestionEleccion.getLista().get(index);
            if (eleccion.getFecha().before(fechaActual)) {
                JOptionPane.showMessageDialog(this, "No se puede modificar una elección cuya fecha ya ha pasado.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (nuevaFecha.before(fechaActual)) {
                JOptionPane.showMessageDialog(this, "La nueva fecha no puede ser anterior a hoy.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            eleccion.setFecha(nuevaFecha);
            eleccion.setTipo(nuevoTipo);
            JOptionPane.showMessageDialog(this, "Elección modificada correctamente.", "Éxito", JOptionPane.INFORMATION_MESSAGE);
            limpiar();
            cargarElecciones();
        } 
        catch (ParseException e) {
            JOptionPane.showMessageDialog(this, "Fecha inválida. Use el formato dd/MM/yyyy.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

